    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // payment:
        try {
            int ri = jTable1.getSelectedRow();
            if (ri != -1) {
                if (p.getDate() != null && !y.getText().equals("") && !a.getText().equals("")) {
                    int r_id = (int) jTable1.getValueAt(ri, 0);
                    String month = (String) m.getSelectedItem();
                    month += "-" + y.getText();
                    int amount = Integer.parseInt(a.getText());
                    java.util.Date dt = p.getDate();
                    java.sql.Date pdate = new java.sql.Date(dt.getTime());
                    db.DbConnect.st.executeUpdate("insert into payments (renter_id,month,amount,payment_date) values(" + r_id + ",'" + month + "'," + amount + ",'" + pdate + "')");
                    getEntries();
                    JOptionPane.showMessageDialog(null, "Payment Added Successfully!");
                    a.setText(null);
                    p.setDate(new java.util.Date());
                } else {
                    JOptionPane.showMessageDialog(null, "Please fill the complete form!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please select any Renter!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

