    private void getEntriesJTable1(){
        try {
            String name = n.getText();
            ResultSet rs = db.DbConnect.st.executeQuery("select * from renters where name like '%" + name + "%' and status='active'");
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            //to remove rows in table
            int rc = dtm.getRowCount();
            while (rc-- != 0) {
                dtm.removeRow(0);
            }
            //to add row in table
            while (rs.next()) {
                java.util.Vector row = new java.util.Vector();
                row.add(rs.getInt("renter_id"));
                row.add(rs.getString("name"));
                row.add(rs.getString("phone"));
                row.add(rs.getString("fname"));
                row.add(rs.getString("room_no"));
                row.add(rs.getInt("rent"));
                row.add(rs.getDate("start_date"));
                dtm.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void getEntries() {
        try {
            int ri = jTable1.getSelectedRow();
            if (ri != -1) {
                javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) jTable2.getModel();
                //to remove rows in table
                int rc = dtm.getRowCount();
                while (rc-- != 0) {
                    dtm.removeRow(0);
                }
                //to add row in table
                int r_id = (int) jTable1.getValueAt(ri, 0);
                ResultSet rs = db.DbConnect.st.executeQuery("select * from payments where renter_id =" + r_id + " order by payment_date DESC ");
                while (rs.next()) {
                    java.util.Vector row = new java.util.Vector();
                    row.add(rs.getInt("payment_id"));
                    row.add(rs.getString("month"));
                    row.add(rs.getDate("payment_date"));
                    row.add(rs.getInt("amount"));
                    dtm.addRow(row);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please select any Renter!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

